package com.uepb.algoritmo;

/**
 * Ponto. Têm dois valores. X e Y
 * @author Lucas Miranda Dourado
 *
 */
public class Ponto {
	int x, y;
	
	public Ponto(int x, int y) {
		this.x = x;
		this.y = y;
	}

	/**
	 * @return the x
	 */
	public int getX() {
		return x;
	}

	/**
	 * @param x the x to set
	 */
	public void setX(int x) {
		this.x = x;
	}

	/**
	 * @return the y
	 */
	public int getY() {
		return y;
	}

	/**
	 * @param y the y to set
	 */
	public void setY(int y) {
		this.y = y;
	}
}
